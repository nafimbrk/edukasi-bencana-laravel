            <h2 class="mt-5 mb-4 font-bold text-lg">Mitigasi Gempa Bumi</h2>

            <p class="mb-4 leading-relaxed">
              Mitigasi adalah upaya yang memiliki sejumlah tujuan yakni untuk
              mengenali risiko, penyadaran akan risiko bencana, perencanaan
              penanggulangan, dan sebagainya. Bisa dikatakan, mitigasi bencana
              adalah segala upaya mulai dari pencegahan sebelum suatu bencana
              terjadi sampai dengan penanganan usai suatu bencana terjadi.
            </p>
            <p class="mb-4 leading-relaxed">
              Gempa Bumi adalah peristiwa berguncangnya bumi yang disebabkan
              oleh tumbukan antar lempeng bumi, aktivitas sesar (patahan),
              aktivitas gunung api, atau runtuhan batuan. Berikut langkah
              mitigasi harus dilakukan baik sebelum, saat, dan pascabencana
              bencana Gempa Bumi:
            </p>
            <p class="mb-4 font-bold">• Sebelum Terjadi Gempa Bumi</p>
            <p class="leading-relaxed">1. Kunci utama adalah:</p>
            <ul class="mb-4 leading-relaxed">
              <li>a) Mengenali apa yang disebut gempa bumi</li>
              <li>
                b) Pastikan bahwa struktur rumah Anda dapat terhindar dari
                bahaya yang disebabkan gempabumi (longsor, rekahan tanah dll)
              </li>
              <li>
                c) Mengevaluasi struktur bangunan Anda agar terhindar dari
                bahaya gempabumi.
              </li>
            </ul>
            <p class="leading-relaxed">2. Kenali lingkungan aanda bekerja</p>
            <ul class="mb-4 leading-relaxed">
              <li>
                a) Perhatikan letak pintu, lift serta tangga darurat, apabila
                terjadi gempabumi sudah mengetahui tempat paling aman untuk
                berlindung.
              </li>
              <li>b) Belajar melakukan P3K.</li>
              <li>c) Belajar menggunakan Pemadam Kebakaran.</li>
              <li>
                d) Mencatat Nomor Telpon Penting yang dapat dihubungi pada saat
                terjadi gempabumi.
              </li>
            </ul>
            <p class="leading-relaxed">
              3. Persiapan rutin pada tempat Anda bekerja dan tinggal
            </p>
            <ul class="mb-4 leading-relaxed">
              <li>
                a) Perabotan (Lemari, Cabinet, dll) diatur menempel pada dinding
                (dipaku/diikat dll) untuk menghindari jatuh, roboh, bergeser
                pada saat terjadi gempabumi.
              </li>
              <li>
                b) Menyimpan bahan yang mudah terbakar pada tempat yang tidak
                mudah pecah, agar terhindar dari kebakaran.
              </li>
              <li>
                c) Selalu mematikan air, gas, dan listrik apabila sedang tidak
                digunakan.
              </li>
            </ul>
            <p class="leading-relaxed">
              4. Penyebab celaka yang paling banyaak pada saat gempa bumi adalah
              akibat kejatuhan material.
            </p>
            <ul class="mb-4 leading-relaxed">
              <li>
                a) Atur benda yang berat sedapat mungkin berada pada bagian
                bawah.
              </li>
              <li>
                b) Cek kestabilan benda yang tergantung yang dapat jatuh pada
                saat gempabumi terjadi (mis: lampu dll).
              </li>
            </ul>
            <p class="leading-relaxed">5. Alat yang harus siap di tempat.</p>
            <ul class="mb-4 leading-relaxed">
              <li>a) Kotak P3K</li>
              <li>b) Senter/lampu battery</li>
              <li>c) Radio</li>
              <li>d) Makanan suplemen</li>
              <li>e) Air</li>
            </ul>
            <p class="mb-4 font-bold">• Saat Terjadi Gempa Bumi</p>
            <p class="leading-relaxed font-bold">
              Jika Anda berada dalam bangunan
            </p>
            <ul class="mb-4 leading-relaxed">
              <li>
                a) Lindungi kepala dan badan Anda dari reruntuhan bangunan.
              </li>
              <img
                src="{{ asset('gambar/gempabumi4.png') }}"
                alt=""
                class="w-full"
              />
              <li>
                b) Mencari tempat yang paling aman dari reruntuhan akibat
                goncangan gempa (seperti di bawah meja, di sudut ruangan yang
                kuat, di bawah kusen, dll).
              </li>
              <img
                src="{{ asset('gambar/gempabumi5.png') }}"
                alt=""
                class="w-full"
              />
              <li>c) Jauhi rak buku, lemari, dan kaca.</li>
              <li>
                d) Hati-hati terhadap langit-langit yang mungkin runtuh,
                benda-benda yang tergantung di dinding dan sebagainya.
              </li>
            </ul>
            <p class="leading-relaxed font-bold">
              Jika berada diluar bangunan atau area terbuka
            </p>
            <ul class="mb-4 leading-relaxed">
              <li>
                a) Menghindari dari bangunan yang ada di sekitar Anda (seperti
                gedung, tiang listrik, pohon dll).
              </li>

              <li>
                b) Perhatikan tempat Anda berpijak, hindari apabila terjadi
                rekahan tanah.
              </li>
            </ul>
            <p class="leading-relaxed font-bold">Jika Anda mengendarai mobil</p>
            <ul class="mb-4 leading-relaxed">
              <li>
                a) Keluar, turun dan menjauh dari mobil, hindari jika terjadi
                rekahan tanah atau kebakaran.
              </li>

              <li>b) Keluar dari mobil dan berlindung di sampingnya.</li>
              <li>
                c) Jangan berhenti diatas/bawah jembatan layang/jembatan
                penyebrangan.
              </li>
            </ul>
            <p class="leading-relaxed font-bold">
              Jika anda tinggal atau berada di pantai
            </p>
            <ul class="mb-4 leading-relaxed">
              <li>
                a) Jauhi pantai menuju ke tempat yang lebih tinggi untuk
                menghindari terjadinya Tsunami.
              </li>
            </ul>
            <p class="leading-relaxed font-bold">
              Jika Anda berada di daerah pegunungan
            </p>
            <ul class="mb-4 leading-relaxed">
              <li>a) Hindari daerah yang mungkin terjadi longsoran.</li>
            </ul>
            <p class="leading-relaxed font-bold">
              Jika Anda berada di dalam lift
            </p>
            <ul class="mb-4 leading-relaxed">
              <li>
                a) Jangan menggunakan lift saat terjadi gempa bumi atau
                kebakaran. Lebh baik menggunakan tangga darurat.
              </li>
              <li>
                b) Jika merasakan gtaran gempa bumi saat berada didalam lift,
                maka tekanlah semua tombol.
              </li>
              <li>
                c) Jika terjebak dalam lift, hubungi manajer gedung dengan
                menggunakan interphone jika tersedia.
              </li>
            </ul>
            <p class="mb-4 font-bold">• Setelah Terjadi Gempa Bumi</p>
            <p class="leading-relaxed">1. Jika Anda bearda di dalam bangunan</p>
            <ul class="mb-4 leading-relaxed">
              <li>
                • Keluar dari bangunan tersebut dengan tertib
                <img
                  src="{{ asset('gambar/gempabumi6.png') }}"
                  alt=""
                  class="w-full mb-"
                />
              </li>
              <li>
                • Jangan menggunakan tangga berjalan atau lift, gunakan tangga
                biasa.
              </li>
              <li>• Periksa apa ada yang terluka, lakukan P3K.</li>
              <li>
                • Telepon atau mintalah pertolongan apabila terjadi luka parah
                pada Anda atau sekitar Anda.
              </li>
            </ul>
            <p class="leading-relaxed">2. Periksa lingkungan sekitar Anda</p>
            <ul class="mb-4 leading-relaxed">
              <li>• Periksa apabila terjadi kebakaran.</li>
              <li>• Periksa apabila terjadi kebocoran gas.</li>
              <li>• Periksa apabila terjadi arus pendek.</li>
              <li>• Periksa aliran dan pipa air.</li>
              <li>
                • Periksa segala hal yang dapat membahayakan (mematikan listrik,
                tidak menyalakan api dll).
              </li>
            </ul>
            <p class="leading-relaxed">
              3. Jangan memasuk bangunan yang sudah terkena gempa, karena
              kemungkinan sewaktu waktu dapat runtuh akibat gempa susulan.
            </p>
            <p class="leading-relaxed">
              4. Jangan berjalan di daerah sekitar gempa, karena kemungkinan
              terjad bahaya sususlan masih ada.
            </p>
            <p class="leading-relaxed">
              5. Mendengarkan informasi Dengarkan informasi mengenai gempa bumi
              dari radio (apabila terjadi gempa susulan). Jangan mudah
              terpancing oleh isu atau berita yang tidak jelas sumbernya.
            </p>
            <p class="leading-relaxed">
              6. Mengisi angket yan diberikan oleh instansi terkait untuk
              mengetahui seberapa besar kerusakan yang terjadi
            </p>
            <p class="leading-relaxed mb-5">
              7. Jangan panik dan jangan lupa selalu berdoa kepada Tuhan YME
              demi keamanan dan keselamatan kita semua.
            </p>

            

            


                    <h2 class="mt-5 mb-4 font-bold text-lg">Mitigasi Bencana Banjir</h2>

                    <p class="leading-relaxed mb-4">
                        Mitigasi bencana adalah serangkaian upaya untuk mengurangi risiko
                        bencana, baik melalui pembangunan fisik maupun penyadaran dan
                        peningkatan kemampuan menghadapi ancaman bencana (Pasal 1 ayat 6
                        PP No 21 Tahun 2008 Tentang Penyelenggaraan Penanggulangan
                        Bencana). Mitigasi terdiri dari tiga bagian yaitu sebagai berikut
                        (Adminbpbd2, 2019) :
                    </p>

                    <p class="font-bold leading-relaxed">1. Pra Bencana</p>
                    <ul class="mb-4 leading-relaxed">
                        <li>
                            a. Mengetahui istilah-istilah peringatan yang berhubungan dengan
                            bahaya banjir, seperti Siaga I sampai dengan Siaga IV dan
                            langkah-langkah apa yang harus dilakukan.
                        </li>
                        <li>
                            b. Mengetahui tingkat kerentanan tempat tinggal kita, apakah di
                            zona rawan banjir (bisa menggunakan aplikasi inarisk).
                        </li>
                        <li>
                            c. Mengetahui cara-cara untuk melindungi rumah kita dari banjir.
                        </li>
                        <li>
                            d. Mengetahui saluran dan jalur yang sering dilalui air banjir
                            dan apa dampaknya untuk rumah kita.
                        </li>
                        <li>
                            e. Melakukan persiapan untuk evakuasi, termasuk memahami rute
                            evakuasi dan daerah yang lebih tinggi.
                        </li>
                        <li>
                            f. Membicarakan dengan anggota keluarga mengenai ancaman banjir
                            dan merencanakan tempat pertemuan apabila anggota keluarga
                            terpencar-pencar.
                        </li>
                        <li>
                            g. Mengetahui bantuan apa yang bisa diberikan apabila ada
                            anggota keluarga yang terkena banjir.
                        </li>
                        <li>
                            h. Mengetahui kebutuhan-kebutuhan khusus anggota keluarga dan
                            tetangga apabila banjir terjadi.
                        </li>
                        <li>
                            i. Membuat persiapan untuk hidup mandiri selama sekurangnya tiga
                            hari, misalnya persiapan tas siaga bencana, penyediaan makanan
                            dan air minum.
                        </li>
                        <li>j. Mengetahui bagaimana mematikan air, listrik dan gas.</li>
                        <li>k. Mempertimbangkan asuransi banjir.</li>
                        <li>
                            l. Berkaitan dengan harta dan kepemilikan, maka anda bisa
                            membuat catatan harta kita, mendokumentasikan dalam foto, dan
                            simpan dokumen tersebut di tempat yang aman.
                        </li>
                        <li>m. Menyimpan berbagai dokumen penting ditempat yang aman.</li>
                        <li>
                            n. Hindari membangun di tempat rawan banjir kecuali ada upaya
                            penguatan dan peninggian bangunan rumah.
                        </li>

                        <li>
                            o. Perhatikan berbagai instrumen listrik yang dapat memicu
                            bahaya saat bersentuhan dengan air banjir.
                        </li>
                        <li>
                            p. Turut serta mendirikan tenda pengungsian dan pembuatan dapur
                            umum
                        </li>
                        <li>q. Melibatkan diri dalam pendistribusian bantuan.</li>
                        <li>r. Menggunakan air bersih dengan efisien.</li>
                    </ul>

                    <p class="font-bold leading-relaxed">2. Saat Bencana</p>
                    <ul class="mb-4 leading-relaxed">
                        <li>
                            a. Apabila banjir akan terjadi di wilayah Anda, maka simaklah
                            informasi dari berbagai media mengenai informasi banjir untuk
                            meningkatkan kesiapsiagaan.
                        </li>
                        <li>
                            b. Apabila terjadi banjir, segeralah evakuasi ke tempat yang
                            lebih tinggi.
                        </li>
                        <li>
                            c. Waspada terhadap arus bawah, saluran air, kubangan, dan
                            tempat-tempat lain yang tergenang air.
                        </li>
                        <li>
                            d. Ketahui risiko banjir dan banjir bandang di tempat Anda,
                            misalnya banjir bandang dapat terjadi di tempat Anda dengan atau
                            tanpa peringatan pada saat hujan biasa atau deras.
                        </li>
                        <li>
                            e. Apabila Anda harus bersiap untuk evakuasi: amankan rumah
                            Anda. Apabila masih tersedia waktu, tempatkan perabot di luar
                            rumah atau di tempat yang aman dari banjir. Barang yang lebih
                            berharga diletakan pada bagian yang lebih tinggi di dalam rumah.
                        </li>
                        <li>
                            f. Matikan semua jaringan listrik apabila ada instruksi dari
                            pihak berwenang. Cabut alat alat yang masih tersambung dengan
                            listrik. Jangan menyentuh peralatan yang bermuatan listrik
                            apabila Anda berdiri di atas/dalam air.
                        </li>
                        <li>
                            g. Jika ada perintah evakuasi dan Anda harus meninggalkan rumah:
                            Jangan berjalan di arus air. Beberapa langkah berjalan di arus
                            air dapat mengakibatkan Anda jatuh.
                        </li>
                        <li>
                            h. Apabila Anda harus berjalan di air, berjalanlah pada pijakan
                            yang tidak bergerak. Gunakan tongkat atau sejenisnya untuk
                            mengecek kepadatan tempat Anda berpijak.
                        </li>
                        <li>
                            i. Jangan mengemudikan mobil di wilayah banjir. Apabila air
                            mulai naik, abaikan mobil dan keluarlah ke tempat yang lebih
                            tinggi. Apabila hal ini tidak dilakukan, Anda dan mobil dapat
                            tersapu arus banjir dengan cepat.
                        </li>
                        <li>
                            j. Bersihkan dan siapkan penampungan air untuk berjaga-jaga
                            seandainya kehabisan air bersih.
                        </li>
                        <li>
                            k. Waspada saluran air atau tempat melintasnya air yang
                            kemungkinan akan dilalui oleh arus yang deras karena kerap kali
                            banjir bandang tiba tanpa peringatan.
                        </li>
                    </ul>

                    <p class="font-bold leading-relaxed">3. Pasca Bencana</p>
                    <ul class="mb-5 leading-relaxed">
                        <li>
                            a. Hindari air banjir karena kemungkinan kontaminasi zat-zat berbahaya dan ancaman
                            kesetrum.
                        </li>
                        <li>
                            b. Waspada dengan instalasi listrik.
                        </li>
                        <li>
                            c. Hindari air yang bergerak.
                        </li>
                        <li>
                            d. Hindari area yang airnya baru saja surut karena jalan bisa saja keropos dan ambles.
                        </li>
                        <li>
                            e. Hindari lokasi yang masih terkena bencana, kecuali jika pihak yang berwenang
                            membutuhkan sukarelawan.
                        </li>
                        <li>
                            f.
                            Kembali ke rumah sesuai dengan perintah dari pihak yang berwenang. </li>
                        <li>
                            g. Tetap di luar gedung/rumah yang masih dikelilingi air. </li>
                        <li>
                            h. Hati-hati saat memasuki gedung karena ancaman kerusakan yang tidak terlihat seperti
                            pada pondasi.
                        </li>
                        <li>
                            i.
                            Perhatikan kesehatan dan keselamatan keluarga dengan mencuci tangan menggunakan
                            sabun dan air bersih jikaAnda terkena air banjir.
                        </li>
                        <li>
                            j. Buang makanan yang terkontaminasi air banjir.
                        </li>
                        <li>
                            k. Dengarkan berita atau informasi mengenai kondisi air, serta di mana mendapatkan
                            bantuan perumahan/shelter, pakaian, dan makanan.
                        </li>
                        <li>
                            l.
                            Dapatkan perawatan kesehatan di fasilitas kesehatan terdekat. </li>
                        <li>
                            m. Bersihkan tempat tinggal dan lingkungan rumah dari sisa-sisa kotoran setelah banjir.
                        </li>
                        <li>
                            n. Lakukan pemberantasan sarang nyamuk Pemberantasan Sarang Nyamuk (PSN).
                        </li>
                        <li>
                            o. Terlibat dalam kaporitisasi sumur gali.
                        </li>
                        <li>
                            p. Terlibat dalam perbaikan jamban dan Saluran Pembuangan Air Limbah (SPAL).
                        </li>
                    </ul>

                    

                    

            <p class="mb-4 leading-relaxed">
              Banjir sungai terjadi saat sungai tidak mampu menampung kapasitas
              air yang besar sehingga terjadi luapan sampai ke area sekitar
              sungai. Terjadinya banjir ini biasanya disebabkan oleh badai hujan
              yang terjadi dalam waktu yang panjang. Faktor lainnya adalah
              kejenuhan air tanah dan efek perubahan iklim pada durasi dan
              intensitas hujan.
            </p>
            <p class="font-bold">3. Banjir Rob</p>
            <p class="mb-4 leading-relaxed">

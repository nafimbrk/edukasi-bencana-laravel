              lingkungan sekitar. Biasanya, banjir lahar ini akan turun ke
              pemukiman warga melalui sungai. Jika sungai tidak bisa menampung
              debit lahar dingin, maka lahar yang membawa bebatuan besar
              tersebut akan menyebar ke berbagai arah.
            </p>

            

            

            <h2 class="mt-5 mb-4 font-bold text-lg">Penyebab Bencana Banjir</h2>

            <p class="leading-relaxed mb-5">
              Banjir yang terjadi di daerah perkotaan umumnya terjadi akibat
              adanya luapan air yang tidak dapat tertampung oleh sistem drainase
              perkotaan seperti sungai, gorong-gorong, parit dan saluran
              pengaliran air lainnya. Perubahan guna lahan di kawasan hulu
              menyebabkan semakin banyak debit air yang menuju ke sistem
              drainase sehingga akan membebani kapasitas sistem drainase
              tersebut. Secara umum, variabel penelitian berupa faktor-faktor
              penyebab banjir yang digunakan dalam penelitian ini adalah sebagai
              berikut (Nugroho and Handayani, 2021) :
            </p>
            <p class="leading-relaxed font-bold">a. Perubahan Guna Lahan</p>
            <img
              src="../../gambar/banjir2.png"
              alt=""
              class="w-full h-80"
            />
            <p class="mb-4 leading-relaxed">
              Banjir disebabkan oleh beberapa faktor, tapi umumnya disebabkan
              oleh adanya perubahan guna lahan di daerah tangkapan air yakni
              daerah hulu atau upland (Hermon, 2015; Rosyidie, 2013).
              Pertambahan jumlah penduduk akibat urbanisasi, tidak teraturnya
              tata ruang perkotaan dan pemanfaatan guna lahan yang tidak sesuai
              dengan rencana tata ruang telah mengakibatkan meningkatnya
              permasalahan banjir di wilayah perkotaan. Hal tersebut disebabkan
              oleh adanya peningkatan kawasan kedap air di area perkotaan
              sehingga menyebabkan peningkatan run off.
            </p>
            <p class="leading-relaxed font-bold">
              b. Curah hujan dan jenis tanah
            </p>
            <img
              src="../../gambar/banjir3.png"
              alt=""
              class="w-full h-80"
            />
            <p class="mb-4 leading-relaxed">
              Menurut Birhanu et al., (2016), banjir di perkotaan semakin
              diperparah oleh adanya hujan lebat dan peristiwa iklim yang
              ekstrem disamping akibat adanya perubahan dramatis terhadap guna
              lahan.
            </p>
            <p class="mb-4 leading-relaxed">
              Pada musim penghujan, curah hujan yang tinggi akan mengakibatkan
              volume air yang masuk ke dalam sistem drainase, misal sungai,
              melebihi kapasitas rencana. Bilamana volume air yang masuk
              melebihi tebing sungai, maka akan menimbulkan banjir atau genangan
              termasuk bobolnya tanggul sungai. Jenis tanah tertentu juga
              memiliki perbedaan respon terhadap curah hujan. Tanah dengan
              tekstur halus memiliki peluang untuk mengalami kejadian banjir
              lebih tinggi daripada tekstur tanah yang lebih kasar. Hal tersebut
              dikarenakan semakin halus tekstur tanah menyebabkan air yang
              berasal dari hujan sulit untuk meresap ke dalam tanah atau
              permeabilitasnya rendah (Sudirman et al., 2014).
            </p>
            <p class="font-bold">c. Tingkat kelerengan</p>

            <p class="mb-4 leading-relaxed">
              Semakin landai kemiringan lereng suatu daerah, maka akan semakin
              besar peluang kawasan tersebut mengalami banjir, demikian pula
              sebaliknya. Semakin curam kemiringan lereng suatu daerah maka akan
              semakin aman kawasan tersebut dari banjir (Darmawan et al., 2017).
              Perubahan kelandaian lahan dari kemiringan lereng curam ke
              kemiringan lereng yang landau atau datar juga akan menciptakan
              daerah Apex yang akan menimbulkan perubahan kecepatan aliran
              permukaan. Hal tersebut yang kemudian akan menimbulkan banjir
              dengan kecepatan aliran permukaan tinggi atau biasa disebut banjir
              bandang (Mulyanto et al., 2012).
            </p>
            <p class="font-bold">d. Erosi dan sedimentasi</p>

            <p class="mb-4 leading-relaxed">
              Akibat perubahan guna lahan maka terjadi erosi yang akan
              mengakibatkan timbulnya sedimentasi pada sistem drainase atau
              sungai. Sedimen masuk ke dalam sistem saluran drainase bersamaan
              dengan aliran air permukaan yang berasal dari hujan. Sedimentasi
              yang masuk ke dalam sistem sungai akan menyebabkan daya tampung
              sungai menjadi berkurang (Kodoatie & Sjarief, 2010). Tutupan lahan
              vegetatif yang rapat seperti semak semak dan rumput merupakan
              penahan laju erosi paling tinggi.
            </p>
            <p class="font-bold">
              e. Kapasitas drainase yang tidak memadai
            </p>

            <p class="mb-4 leading-relaxed">
              Pengurangan kapasitas tampung drainase disebabkan oleh adanya
              sedimentasi dan faktor lain yang disebabkan oleh manusia, seperti
              tersumbatnya saluran akibat sampah yang dibuang secara sengaja ke
              dalam sistem drainase (Douglas et al., 2008). Hal tersebut
              menyebabkan volume air yang dapat tertampung berada di bawah
              volume rencana kapasitas drainase yang seharusnya. Di samping itu,
              penurunan kapasitas drainase dapat disebabkan oleh adanya bangunan
              yang berada di sempadan sungai sehingga menghambat aliran dan
              menyulitkan operasi pemeliharaan sungai (Kodoatie & Sjarief, 2010)
            </p>
            <p class="leading-relaxed font-bold">
              f. Tindakan membuang sampah di sungai atau saluran air.
            </p>
            <img
              src="../../gambar/banjir4.png"
              alt=""
              class="w-full h-80"
            />
            <p class="mb-4 leading-relaxed">
              Tindakan membuang sampai di sungai atau saluran air kerap kali
              dilakukan oleh masyarakat terutama yang rumahnya dekat dengan
              sungai atau saluran air. Hal ini diakibatkan urangnya kesadaran
              masyarakat akan dampak yang ditimbulkan dari tindakan tersebut.
              Pemerintah perlu menggalakkan program pelarangan membuang sampah
              disungai agar bencana banjir dapat dicegah.
            </p>

            <p class="mb-4 leading-relaxed">
              Menurut Kodatie dan Sugiyanto (2002) faktor penyebab terjadinya
              banjir dapat di klasifikasikan dalam dua kategori yaitu sebagai
              berikut:
            </p>
            <p class="leading-relaxed">1. Penyebab banjir secara alami :</p>

            <ul class="mb-4 leading-relaxed">
              <li>a. Curah hujan</li>
              <li>b. Pengaruh fisiografi</li>
              <li>c. Erosi dan Sedimentasi</li>
              <li>d. Kapasitas sungai</li>
              <li>e. Kapasitas drainase yang tidak memadai</li>
              <li>f. Pengaruh air pasang</li>
            </ul>
            <p class="leading-relaxed">
              2. Penyebab banjir akibat aktivitas manusia :
            </p>

            <ul class="mb-5 leading-relaxed">
              <li>a. Perubahan kondisi DAS (Daerah Alliran Sungai)</li>
              <li>b. Kawasan kumuh dan sampah</li>
              <li>c. Drainase perkotaan dan pengembangan pertanian</li>
              <li>d. Kerusakan bangunan pengendali air</li>
              <li>e. Perencanaan sistem pengendalian banjir tidak tepat</li>
              <li>f. Rusaknya hutan (hilangnya vegetasi alami)</li>
            </ul>

            

            

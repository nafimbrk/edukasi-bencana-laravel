            <h2 class="mt-5 mb-4 font-bold text-lg">Penyebab Bencana Kebakaran</h2>

            <p class="mb-4 leading-relaxed">
              Kebakaran disebabkan oleh berbagai faktor, yaitu oleh manusia
              secara langsung maupun tidak langsung atau dapat disebabkan oleh
              alam. Api yang dapat memicu kebakaran juga memiliki berbagai
              sumber penyalaan, tidak hanya berasal dari sumber api secara
              langsung tetapi sumber api dapat disebabkan dari berbagai kegiatan
              manusia yang secara tidak langsung dapat menimbulkan api (Seri
              LPPS, 2001). Kebakaran yang disebabkan oleh faktor alam yaitu
              petir, gempa bumi, letusan gunung berapi dan kekeringan, sedangkan
              kebakaran yang disebabkan oleh faktor manusia biasanya disebabkan
              akibat kelalaian diantaranya (Geografi, 2016) :
            </p>
            <dl class="mb-4 leading-relaxed">
              <dt class="font-bold">a. Instalasi listrik</dt>
              <dd class="mb-4">
                Kebakaran yang sering terjadi disebabkan oleh instalasi listrik
                karena pemasangan instalasi yang tidak sempurna, penggunaan alat
                atau instalasi yang tidak standar atau kurang aman, penggunaan
                listrik dengan cara tidak aman, serta penggunaan peralatan yang
                tidak baik atau rusak.
              </dd>
              <dt class="font-bold">b. Peralatan memasak</dt>
              <dd class="mb-4">
                Penyebab kebakaran yang potensial di lingkungan rumah adalah
                dari alat masak, baik gas, kompor minyak tanah maupun listrik.
                Banyak pengguna gas LPG yang kurang paham cara penggunaan gas
                yang aman.
              </dd>
              <dt class="font-bold">c. Perilaku Penghuni</dt>
              <dd>
                Kebakaran juga sering terjadi karena perilaku penghuni, misalnya
                menyalakan api untuk penerangan ditempat penyimpanan bahan bakar
                (bensin) yang mudah terbakar, menempatkan obat nyamuk, lilin,
                lampu teplok yang sedang menyala ditempat yang mudah terbakar,
                atau menggunakan peralatan listrik berlebihan melampaui beban
                yang aman (Ramli, 2010).
              </dd>
            </dl>


            <h2 class="mt-5 mb-4 font-bold text-lg">Apa Itu Kebakaran</h2>
            

            <p class="mb-4 leading-relaxed">
                Kebakaran adalah suatu reaksi oksidasi eksotermis yang berlangsung dengan cepat dari 
                suatu bahan bakar yang disertai dengan timbulnya api/penyalaan (“DEFINISI / 
                PENGERTIAN KEBAKARAN,” 2012). Menurut National Fire Protection Association 
                (NFPA) kebakaran didefinisikan sebagai peristiwa oksidasi yang melibatkan tiga unsur yang 
                harus ada, yaitu bahan bakar yang mudah terbakar, oksigen yang ada dalam udara dan sumber 
                energy atau panas yang berkaitan menimbulkan kerugian harta benda, cedera bahkan 
                kematian (Pradipta, 2016, pp. 14–15).
            </p>
            <p class="mb-4 leading-relaxed">
                Dewan Keselamatan dan Kesehatan Kerja Nasional (DK3N) mendefinisikan kebakaran 
                sebagai suatu peristiwa bencana yang berasal dari api yang tidak dapat dikehendaki yang 
                dapat menimbulkan kerugian, baik kerugian materi (berupa harta benda, banguan fisik, depot, 
                fasilitas sarana dan prasarana) mupun kerugian yang nonmateri (seperti rasa takut, trauma) 
                hingga  kehilangan nyawa atau cacat tubuh yang ditimbulkan akibat kebakaran (Pradipta, 
                2016, p. 15). 
            </p>
            <p class="mb-5 leading-relaxed">
                Dari beberapa pengertian kebakaran yang telah terpapar dapat disimpulkan bahwa 
                kebakaran adalah sebuah api/penyalaan yang mengenai unsur yang mudah terbakar, hingga 
                dapat mengalami kerusakan, kehancuran kerugian material dan cacat tubuh yang di akibatkan 
                oleh kebakaran serta dapat menyebabkan rasa takut dan trauma. Berbagai dampak bencana di 
                tingkat masyarakat disebabkan oleh beberapa hal, antara lain Perubahan dinamika kehidupan 
                keluarga, hilangnya fasilitas penting, memberikan beban pada sekelompok masyarakat 
                tertentu, dan menyebabkan perubahan pada produktifitas Masyarakat (Hidayati et al., 2020).                 
            </p>
            
             

            

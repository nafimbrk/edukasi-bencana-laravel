                       Kamis (13/06/2024) Pada pertemuan kedua, diadakan penyuluhan yang bertajuk “Mitigasi Bencana Banjir dan Kebakaran”. Selain itu, juga diadakan pembentukan kader yang diambil dari santri dan santriwati. Penyuluhan ini bertujuan untuk memberikan pemahaman serta menjadikan para santri dan santriwati agar selalu siap, sigap, dan tanggap dalam menghadapi segala macam bencana.
                       </p>
                    <p class="mb-4 leading-relaxed">Acara penyerahan dimulai pukul 20.00 WIB dengan sedikit penyampaian
                        Sosialisasi ini dilaksanakan di Aula Gedung Putri 1 dan dihadiri oleh puluhan santri dan santriwati yang tentunya memiliki semangat luar biasa dalam mengikuti kegiatan ini. Sebelum menyampaikan materi, Ia memulai dengan memberi kuisioner berupa Pre-Test yang berisi pertanyaan-pertanyaan terkait kebencanaan, guna untuk mengetahui tingkat pengetahuan santri dan santriwati. Dilanjutkan dengan pemaparan materi.
                                               </p>
                    <p class="mb-4 leading-relaxed">Adapun alat-alat siaga bencana yang diserahkan meliputi Rompi, Helm
                       “Banjir adalah peristiwa meningkatnya volume air sehingga menyebabkan meluapnya air hingga kedaratan,” terang Kak Koko dan Kak Laura yang bertugas menyampaikan materi seputar banjir. Para santri dan santriwati terlihat antusias mendengarkan materi. “Bencana banjir itu disebabkan oleh 2 faktor, yakni faktor alam dan faktor manusia, contoh yang paling umum dari faktor manusia ialah membuang sampah disungai dan penebangan pohon sembarangan. Contoh dari faktor alam ialah erosi tanah, sebenarnya keduanya itu saling berkaitan maka perlu adanya upaya untuk meminimalisir dan mencegahnya, yaitu dengan melakukan sosialisasi bencana, rajin membersihkan saluran air serta melakukan reboisasi dan tebang pilih,” lanjutnya.
</p>
                    <p class="mb-4 leading-relaxed">Kemudian dilanjutkan dengan materi seputar kebakaran. “Kebakaran adalah bencana yang berasal dari api yang tak dapat dikehendaki dan menimbulkan kerugian materi, maupun non materi. Kebakaran biasanya disebabkan oleh membuang putung rokok sembarangan dan bermain dengan benda pemicu api, bila terjadi kebakaran yang paling pertama dilakukan yaitu jangan panik, jika ada asap merunduklah karna udara yang bersih ada dibawah dan jika melewati api tutuplah kepala dan badan dengan selimut atau kain basah. Kemudian upaya untuk mencegah kebakaran ialah tidak merokok di dekat bahan yang mudah terbakar, merapikan instalasi listrik serta menjauhkan benda padat seperti kayu, dan karet dari sumber api,” jelas Kak Caca dan Kak Nailah.
</p>
                    <p class="mb-4 leading-relaxed">Sebelum Kak Rama mengakhiri acara, dilakukan sesi tanya jawab. Namun kali ini agak berbeda, karena Kakak Pembinalah yang bertanya kepada para santri dengan cara menununjuk salah satu santri dengan cara acak. Tentu hal tersebut membuat para santri dan santriwati deg-deg an karena takut ditunjuk oleh Kakak Pembina.

                    </p>
                    <p class="mb-4 leading-relaxed">Pertanyaan yang pertama dilanturkan oleh Kak Tya selaku pendamping santriwati di pondok. “Mengapa penilitian ini diambil dari Pondok Pesantren?,” ujar Kak Tya. “Karena di Pondok Pesantren ini rentan terjadinya bencana, juga dihuni dengan banyak jiwa, jadi perlu dilakukan sosialisasi terkait kebencanaan,” jawab Kak Rama.

